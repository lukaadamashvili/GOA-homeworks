# input() - მომხმარებლისგან ინფორმაციის მიღება
# type() - ცვლადის მონაცემთა ტიპის დადგენა
# int() - მნიშვნელობის მთელი რიცხვად გარდაქმნა
# float() - მნიშვნელობის ათწილადად გარდაქმნა
# str() - მნიშვნელობის ტექსტად გარდაქმნა
