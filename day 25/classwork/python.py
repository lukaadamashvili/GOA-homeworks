# 1) შექმენით ცვლადები სხვადასხვა ტიპით: str, int, float, bool
name = "Luka"          # str
age = 13               # int
height = 1.70          # float
is_captain = True      # bool

print(name, type(name))
print(age, type(age))
print(height, type(height))
print(is_captain, type(is_captain))


# 2) ჰკითხე მომხმარებელს სახელი, ასაკი და საყვარელი რიცხვი
user_name = input("შეიყვანე შენი სახელი: ")
user_age = input("შეიყვანე შენი ასაკი: ")
fav_number = input("შეიყვანე შენი საყვარელი რიცხვი: ")

print("სახელი:", user_name, type(user_name))
print("ასაკი:", user_age, type(user_age))
print("საყვარელი რიცხვი:", fav_number, type(fav_number))

# ასაკი და საყვარელი რიცხვი გადავიყვანოთ რიცხვებად
user_age = int(user_age)
fav_number = float(fav_number)

# დაბეჭდეთ მათი ჯამი
print("ასაკისა და საყვარელი რიცხვის ჯამი არის:", user_age + fav_number)


# 3) შექმენი რიცხვების დიაპაზონი 5-დან 100-მდე
for number in range(5, 101):
    if number % 2 == 0:
        print(number, "- ლუწია", "-", type(number))
    else:
        print(number, "- კენტია", "-", type(number))


# 4) კომენტარის სახით ახსენით რაში გამოიყენება ფუნქციები
# ფუნქციები გამოიყენება იმისთვის, რომ კოდი გავამარტივოთ და ერთი და იგივე მოქმედება რამდენჯერმე მარტივად გამოვიყენოთ.
