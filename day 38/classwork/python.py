# =========================
# 5) რა არის unpacking + 5 მაგალითი
# =========================

# unpacking ნიშნავს tuple-ში არსებული მნიშვნელობების
# ცალკეულ ცვლადებად "დაშლას"

# მაგალითი 1
person = ("Luka", 17, "Georgia")

name, age, country = person
print(name)
print(age)
print(country)

#  მაგალითი 2 
numbers = (10, 20)

a, b = numbers
print(a)
print(b)

# მაგალითი 3
colors = ("red", "green", "blue")

c1, c2, c3 = colors
print(c1, c2, c3)

# მაგალითი 4 
# unpacking list-ზეც მუშაობს

values = [1, 2, 3]

x, y, z = values
print(x, y, z)

# მაგალითი 5 
# _ გამოიყენება მაშინ, როცა რაღაც მნიშვნელობა არ გვჭირდება

data = ("Luka", 17, "Football")

name, _, hobby = data
print(name)
print(hobby)
