# tuple იწერება მრგვალი ფრჩხილებით ()
# list იწერება ოთხკუთხე ფრჩხილებით []

# მთავარი განსხვავება:
# tuple არის უცვლელი (immutable) → შექმნის შემდეგ მისი შეცვლა აღარ შეიძლება
# list არის ცვლადი (mutable) → შეგვიძლია ელემენტების შეცვლა, დამატება, წაშლა

# tuple გამოიყენება მაშინ, როცა გვინდა მონაცემები იყოს დაცული ცვლილებისგან


# =========================
# 2) Tuple ადამიანების სახელებით + slicing
# =========================

# ვქმნით tuple-ს, სადაც შევინახავთ ადამიანების სახელებს
names = ("Giorgi", "Nika", "Luka", "Ana", "Mariam")

# slicing ნიშნავს მონაცემების ნაწილის ამოღებას
# [1:-1] ნიშნავს:
# დავიწყოთ პირველი ინდექსიდან (მეორე ელემენტი)
# დავამთავროთ ბოლო ელემენტამდე (ბოლოს არ ჩათვლით)

middle_names = names[1:-1]

# ვბეჭდავთ შედეგს
print(middle_names)


# =========================
# 3) tuple-ის მეთოდები (methods)
# =========================

# tuple-ს მხოლოდ 2 ძირითადი მეთოდი აქვს,
# რადგან ის უცვლელია

# --- count() ---
# count() ითვლის რამდენჯერ გვხვდება კონკრეტული მნიშვნელობა tuple-ში

numbers = (1, 2, 3, 2, 2, 4)

count_of_two = numbers.count(2)  # დაითვლის რამდენჯერ არის 2
print(count_of_two)

# --- index() ---
# index() აბრუნებს იმ ელემენტის ინდექსს,
# რომელიც პირველად შეხვდება tuple-ში

index_of_three = numbers.index(3)  # იპოვის რიცხვი 3-ის ინდექსს
print(index_of_three)


